package model;

public class Canto {
  private String title;
  private String href;
  private String in;
  private String gr;
  private String al;
  private String of;
  private String co;         
  private String hy;
  private String tr;
  private String trSept;
  private String alPasch;
  private String ref;	
  private String seq;
  
  //M.E

    public Canto(String title, String href, String in, String gr, String al, String of, String co, String hy, String tr, String trSept, String alPasch, String ref, String seq) {
        this.title = title;
        this.href = href;
        this.in = in;
        this.gr = gr;
        this.al = al;
        this.of = of;
        this.co = co;
        this.hy = hy;
        this.tr = tr;
        this.trSept = trSept;
        this.alPasch = alPasch;
        this.ref = ref;
        this.seq = seq;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }

    public String getIn() {
        return in;
    }

    public void setIn(String in) {
        this.in = in;
    }

    public String getGr() {
        return gr;
    }

    public void setGr(String gr) {
        this.gr = gr;
    }

    public String getAl() {
        return al;
    }

    public void setAl(String al) {
        this.al = al;
    }

    public String getOf() {
        return of;
    }

    public void setOf(String of) {
        this.of = of;
    }

    public String getCo() {
        return co;
    }

    public void setCo(String co) {
        this.co = co;
    }

    public String getHy() {
        return hy;
    }

    public void setHy(String hy) {
        this.hy = hy;
    }

    public String getTr() {
        return tr;
    }

    public void setTr(String tr) {
        this.tr = tr;
    }

    public String getTrSept() {
        return trSept;
    }

    public void setTrSept(String trSept) {
        this.trSept = trSept;
    }

    public String getAlPasch() {
        return alPasch;
    }

    public void setAlPasch(String alPasch) {
        this.alPasch = alPasch;
    }

    public String getRef() {
        return ref;
    }

    public void setRef(String ref) {
        this.ref = ref;
    }

    public String getSeq() {
        return seq;
    }

    public void setSeq(String seq) {
        this.seq = seq;
    }
  
  
}
